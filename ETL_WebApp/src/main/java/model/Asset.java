package model;

public class Asset {
    private int assetId;
    private String assetNumber;
    private String itemText;

    public int getAssetId() { return assetId; }
    public void setAssetId(int assetId) { this.assetId = assetId; }

    public String getAssetNumber() { return assetNumber; }
    public void setAssetNumber(String assetNumber) { this.assetNumber = assetNumber; }

    public String getItemText() { return itemText; }
    public void setItemText(String itemText) { this.itemText = itemText; }
}
